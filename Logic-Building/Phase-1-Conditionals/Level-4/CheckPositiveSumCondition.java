public class CheckPositiveSumCondition {

    public static void main(String[] args) {
        int num1 = 5; // Example number 1
        int num2 = -3; // Example number 2

        if (num1 + num2 > 0) {
            System.out.println("The sum of " + num1 + " and " + num2 + " is positive.");
        } else {
            System.out.println("The sum of " + num1 + " and " + num2 + " is not positive.");
        }
    }
}